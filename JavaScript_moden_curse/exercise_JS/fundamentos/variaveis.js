// para criar uma váriavel é necessário adicionar um objeto chamado var;
var exemplo = "Qualquer coisa que eu queira";
/*
    uma var é uma instancia na qual pode ser
    chamada a qualquer momento e seu resultado pode
    ser alterado em qualquer determinado momento
*/

let exemplo02 = "Qualquer outra coisa"
/*
    um let é uma instancia na qual pode ser
    chamada a qualquer momento tambem porém
    é de atribuição unica ou seja, só pode
    ser criada uma vez, e depois alterar ou
    adicionar o resultado normalmente
*/

// para criar uma constante é necessário adicionar um objeto chamado const
const exemplo03 = "... mesma regra";
/*
    uma constante é uma instancia que diferente
    das outras não pode ter seu valor alterado
    posteriormente
*/